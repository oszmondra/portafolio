    <h4>Semana 9 - Introducción a POO</h4>
    <p></p>
    <p>La Programación Orientada a Objetos (POO u OOP según sus siglas en inglés) es un paradigma de programación que usa
      objetos y sus interacciones para diseñar aplicaciones y programas de computadora. Está basado en varias técnicas,
      incluyendo herencia, modularidad, polimorfismo, y encapsulamiento. Su uso se popularizó a principios de la década
      de 1990. Actualmente son muchos los lenguajes de programación que soportan la orientación a objetos. 
      El término de Programación Orientada a Objetos indica más una forma de diseño y una metodología de desarrollo de 
      software que un lenguaje de programación, ya que en realidad se puede aplicar el Diseño Orientado a Objetos (En inglés 
      abreviado OOD, Object Oriented Design), a cualquier tipo de lenguaje de programación.</p>
    <h6>Abstracción</h6>
    <li>La abstracción es un proceso de interpretación y diseño que implica reconocer y enfocarse en las características 
      importantes de una situación u objeto, y filtrar o ignorar todas las particularidades no esenciales.</li>
    <li>Dejar a un lado los detalles de un objeto y definir las características específicas de éste, aquellas que lo distingan 
      de los demás tipos de objetos.</li>
    <li>Hay que centrarse en lo que es y lo que hace un objeto, antes de decidir cómo debería ser implementado.</li>     
    <li>Se hace énfasis en el qué hace más que en el cómo lo hace.</li>
    <p></p>
    <h6>Encapsulamiento</h6>
    <li>Es la propiedad que permite asegurar que la información de un objeto está oculta del mundo exterior.</li>
    <li>El encapsulamiento consiste en agrupar en una Clase las características(atributos) con un acceso privado y los
      comportamientos (métodos) con un acceso público.</li>
    <li>Acceder o modificar los miembros de una clase a través de sus métodos.</li>
    <p></p>
    <h6>Herencia</h6>
    <li>Es una propiedad que permite crear clases a partir de otras ya existentes, obteniendo características (métodos y atributos)
      similares a los ya existentes.</li>
    <li>En otras palabras, la nueva clase (subclase o clase derivada) puede utilizar la misma implementación de su superclase 
      (clase base) o especificar una nueva implementación.</li>
