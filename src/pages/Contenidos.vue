    <b>DO-WHILE</b>
    <p>Es un bucle de control de salida, lo que significa que primero ingresa al bucle, ejecuta las declaraciones y luego verifica 
      la condición. Por lo tanto, una declaración se ejecuta al menos una vez al usar el bucle do ... while. Después de ejecutar
      una vez, el programa se ejecuta siempre que la condición sea verdadera.</p>
