    <b>FOR</b>
    <p>Este tipo de bucles se utiliza cuando el usuario sabe de antemano cuántas veces debe ejecutarse el bloque.
      Es decir, el número de iteraciones se conoce de antemano. Este tipo de bucles también se conocen como bucles controlados
