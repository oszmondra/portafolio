    <h4>Semana 10 - Clases Abstratas e Interfaces</h4> 
    <p></p>
    <h6>Clases abstractas</h6>
    <li>Las clases abstractas no pueden ser instanciadas.</li>
    <li>Pueden declarar la existencia de los métodos, pero no su implementación.</li>
    <li>Puede contener métodos no-abstractos, pero debe de contener al menos un método abstracto.</li>
    <li>Los métodos abstractos deben ser definidos en las clases heredadas, pero deben tener la misma visibilidad.</li>
    <li>La implementación debe tener la misma “firma”: la declaración de tipos y el número de argumentos.</li>
    <li>Las clases abstractas están implementadas desde la versión 5.4.0.</li>
    <p></p>
    <h6>Interfaces</h6>
    <li>Las interfaces nos permiten crear código con el cual especificar qué métodos deben ser implementados
      por una clase (prototipos), sin definir su implementación.</li>
    <li>Las interfaces se definen con la palabra reservada interface.</li>
    <li>Los métodos declarados en una interfaz deben ser públicos.</li>
    <li>Para implementar una interface desde una clase se utiliza la palabra reservada implements.</li>
    <li>Una clase puede implementar más de una interface si se desea.</li>







